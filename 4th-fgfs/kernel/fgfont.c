FGFONT_PIXMAP *fg_scale_font(FGFONT_PIXMAP *font,FG_INT32 width,FG_INT32 height)
{
  FG_INT32 i,j,k,j1,k1;
  char text[128];
  FGFONT_PIXMAP *pixmap=(FGFONT_PIXMAP *)calloc(1,sizeof(FGFONT_PIXMAP));
  pixmap->name=malloc(32);
  sprintf(text,"fgfont_%dx%d",width,height);
  fprintf(stderr,"Scaling %s to %s\n",font->name,text);
  strncpy(pixmap->name,text,32);
  pixmap->width=width;
  pixmap->height=height;
  pixmap->pixmap[0]=calloc(256,width*height);
  for (i=1;i<256;i++) {
	 pixmap->pixmap[i]=pixmap->pixmap[i-1]+(width*height);
	 for (j=0; j<height;j++) {
		j1=(j*font->height)/height;
		for (k=0; k<width; k++) {
        k1=(k*font->width)/width;
		  pixmap->pixmap[i][j*width+k]=font->pixmap[i][j1*font->width+k1];
		}
	 }
  }
  return (pixmap);
}

FGFONT_PIXMAP *fg_get_font(FG_INT32 width,FG_INT32 height)
{
  FG_INT32 i,wdiff,hdiff;
  FG_INT32 min_index=0;
  FG_INT32 mindiff=INT_MAX;

  for (i=0;i<num_loaded_fonts;i++) {
	 wdiff=width-loaded_fonts[i]->width;
	 hdiff=height-loaded_fonts[i]->height;
	 if ((wdiff>=0) && (hdiff>=0))
    {
      if ((wdiff+hdiff)<mindiff) 
		{
        mindiff=wdiff+hdiff;
        min_index=i;
      }
      if ((wdiff<=(width/8)) && (hdiff<=(width/8)))
      {
        return (loaded_fonts[i]);
      } 
	 }
  }
  loaded_fonts[num_loaded_fonts]=
	 fg_scale_font(loaded_fonts[min_index],width,height);
  return (loaded_fonts[num_loaded_fonts++]);
} 
  

FGFONT_PIXMAP *fg_load_font(FGFONT_BITS *fontbits)
{
  FG_INT32 i,j,k,b,skipwidth,endpix;
  FGFONT_PIXMAP *pixmap=(FGFONT_PIXMAP *)calloc(1,sizeof(FGFONT_PIXMAP));

  pixmap->name=fontbits->name;
  pixmap->width=fontbits->width;
  pixmap->height=fontbits->height;
  pixmap->pixmap[0]=calloc(256,fontbits->width*fontbits->height);

  for (i=1;i<256;i++) {
    pixmap->pixmap[i]=pixmap->pixmap[i-1]+((fontbits->width)*(fontbits->height));
    for (j=0;j<fontbits->height;j++)
    {
      b=FG_MIN((1<<(fontbits->width-1)),128);
      skipwidth=FG_MAX(0,(fontbits->width-8)/2);
		endpix=fontbits->width-skipwidth;
      for (k=skipwidth;k<=endpix;k++)
      {
        if (b & fontbits->bmp[i][j]) 
          pixmap->pixmap[i][j*(fontbits->width)+k]=0xff;
        b>>=1;
      }
	 }
  }
  loaded_fonts[num_loaded_fonts++]=pixmap;
  
  return (pixmap);
}



void fgfont_init()
{
  fgfont_4x6=fg_load_font(&fgfontb_4x6);
  fgfont_8x8=fg_load_font(&fgfontb_8x8);
  fgfont_9x14=fg_load_font(&fgfontb_9x14);
  fgfont_10x16=fg_load_font(&fgfontb_10x16);
}

void fg_drawtext(char *text, FGFONT_PIXMAP *font, FG_INT32 x, FG_INT32 y, FG_INT32 color, 
                 FG_BITMAP *bmp) 
{
  FG_INT32 minx,maxx,miny,maxy;
  FG_INT32 i,j,k;
  FG_INT32 n_chars=strlen(text);

  if (bmp->clip) 
  {
	 miny=FG_MIN(FG_MAX(y,bmp->ct),bmp->cb);
    maxy=FG_MAX(FG_MIN(y+font->height-1,bmp->cb),bmp->ct);
  }
  for (i=0;i<n_chars;i++) {
	 minx=FG_MIN(FG_MAX(x,bmp->cl),bmp->cr);
    maxx=FG_MAX(FG_MIN(x+font->width-1,bmp->cr),bmp->cl);
	 for (j=0;j<=(maxy-miny);j++) {
       if (minx<maxx) {
         for (k=minx;k<=maxx;k++) 
           bmp->plot(k,miny+j, 
             COLOR_INDEX(font->pixmap[(FG_INT32)(text[i])][j*font->width+k-minx] & color),bmp);
      }
    }
	 x+=font->width;
  }
}


